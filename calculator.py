class Calc:
    def __init__(self):
        pass

    def multiply(self, x, y):
        return x * y
      
    def add(self, x: int, y: int) -> int:
        """addition of two numbers
        
        Args:
            x (int): first number
            y (int): second number

        Returns:
            int: result of addition
        """
        return x + y